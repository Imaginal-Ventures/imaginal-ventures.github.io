// Fetches the Imaginal Substack RSS feed and writes the 3 most recent posts
// to docs/notes-data.js as window.NOTES_DATA.
// Run on a schedule by .github/workflows/update-substack-notes.yml.

const FEED_URL = "https://imaginalventures.substack.com/feed";
const OUT_PATH = new URL("../docs/notes-data.js", import.meta.url);

const ROMAN_MONTHS = ["I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X", "XI", "XII"];

function decodeEntities(str) {
  return str
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&#8217;/g, "’")
    .replace(/&#8216;/g, "‘")
    .replace(/&#8220;/g, "“")
    .replace(/&#8221;/g, "”")
    .replace(/&#8211;/g, "–")
    .replace(/&#8212;/g, "—");
}

function extractTag(block, tag) {
  const cdata = block.match(new RegExp(`<${tag}><!\\[CDATA\\[([\\s\\S]*?)\\]\\]><\\/${tag}>`));
  if (cdata) return decodeEntities(cdata[1].trim());
  const plain = block.match(new RegExp(`<${tag}>([\\s\\S]*?)<\\/${tag}>`));
  return plain ? decodeEntities(plain[1].trim()) : "";
}

function formatDate(pubDate) {
  const d = new Date(pubDate);
  const year = d.getUTCFullYear();
  const month = ROMAN_MONTHS[d.getUTCMonth()];
  const day = String(d.getUTCDate()).padStart(2, "0");
  return `${year} · ${month} · ${day}`;
}

async function main() {
  const res = await fetch(FEED_URL);
  if (!res.ok) throw new Error(`Feed fetch failed: ${res.status}`);
  const xml = await res.text();

  const itemBlocks = xml.match(/<item>[\s\S]*?<\/item>/g) || [];
  if (itemBlocks.length === 0) throw new Error("No <item> entries found in feed");

  const allPosts = itemBlocks.map((block) => ({
    title: extractTag(block, "title"),
    href: extractTag(block, "link"),
    pubDate: extractTag(block, "pubDate")
  }));

  const total = allPosts.length;
  const top3 = allPosts.slice(0, 3).map((post, i) => ({
    n: `No. ${String(total - i).padStart(3, "0")}`,
    date: formatDate(post.pubDate),
    title: post.title,
    href: post.href
  }));

  const banner =
    "// AUTO-GENERATED by scripts/update-substack-notes.mjs via GitHub Actions.\n" +
    "// Do not edit by hand — changes will be overwritten on the next scheduled run.\n";
  const contents = `${banner}window.NOTES_DATA = ${JSON.stringify(top3, null, 2)};\n`;

  await import("node:fs/promises").then((fs) => fs.writeFile(OUT_PATH, contents, "utf8"));
  console.log(`Wrote ${top3.length} posts to ${OUT_PATH.pathname}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
